double SquareMeter(int iValue)
{
    double Meter = iValue*0.0929;
    return Meter;
}

int main()
{
    int iValue =0;
    double iRet = 0;

    cout<<"Enter area in square feet";
    cin>>iValue;

    iRet=SquareMeter(iValue);
    cout<<" squareMeter : "<<iRet;

    return  0;
}
