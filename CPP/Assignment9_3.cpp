void DisplayDigit(int iNo)
{
    int iDigit = 0;

    if(iNo<0)
    {
        iNo=-iNo;
    }
    while (iNo!=0)
    {
        iDigit=iNo%10;
        cout<<iDigit<<endl;
        iNo = iNo / 10;
    }
}

int main()
{
    int iValue = 0;

    cout<<"Enter number";
    cin>>iValue;

    DisplayDigit(iValue);

    return 0;
}
