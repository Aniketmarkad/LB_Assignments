#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
// 
// Function Name : Display
// Description   : This function is used print pattern using Recursion. 
// Input         : int
// Output        : int
// Author        : Aniket Devaba Markad 
// Date          : 20/07/2025 
// 
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 

void Display()
{
    static int iCnt = 1;

    if(iCnt <= 5)
    {
        printf(" %d \t",iCnt);
        iCnt++;

        Display();
    }

}

int main()
{

    Display();
   
    return 0 ;
}