#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
// 
// Function Name : Display
// Description   : This function is used print pattern using Recursion. 
// Input         : int
// Output        : int
// Author        : Aniket Devaba Markad 
// Date          : 20/07/2025 
// 
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 

void Display()
{
    static int iNo = 5;
    if(iNo >= 1)
    {
        printf(" %d \t",iNo);
        iNo--;
        Display();
    }

}

int main()
{

    Display();
   
    return 0 ;
}