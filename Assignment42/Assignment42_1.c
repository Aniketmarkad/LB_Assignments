#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
// 
// Function Name : Display
// Description   : This function is used print pattern using Recursion. 
// Input         : int
// Output        : int
// Author        : Aniket Devaba Markad 
// Date          : 7/08/2025 
// 
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 

void Display(int iNo)
{ 

   if(iNo >= 1)
   {
     printf(" %d \t * \t",iNo);
     iNo--;
     Display(iNo);
   }
}

int main()
{
    int iValue = 5;

    Display(iValue);

    return 0;
}