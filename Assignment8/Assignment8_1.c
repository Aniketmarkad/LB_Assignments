#include<stdio.h>
#define PI 3.14

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
// 
// Function Name : CircleArea
// Description   : This function is used to calculate area of circle. 
// Input         : float 
// Output        : double
// Author        : Aniket Devaba Markad 
// Date          : 16/05/2025 
// 
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 

double CircleArea(float fRadius)
{   
     double Area = 0;
     Area = PI * fRadius * fRadius;
     return Area;
}

int main()
{
    float fValue = 0.0;
    double dRet = 0.0;

    printf("Enter radius");
    scanf("%f",&fValue);

    dRet=CircleArea(fValue);
    printf("Area of circle : %lf",dRet);

    return  0;

}